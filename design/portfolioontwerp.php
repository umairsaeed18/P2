                <strong>Project</strong> <br><br>
                Naast de stijlgids heb ik ook wireframes gemaakt van mijn portfolio dat heb ik gedaan om de
                paginalay-out en opstelling van de inhoud van de website te weergeven, inclusief interface-elementen en
                navigatiesystemen, en hoe ze samenwerken. Ik heb voor bijna alle pagina's een ontwerp gemaakt, zodat ik
                tijdens het coderen niet veel over de lay-out hoef na te denken. Ik heb mijn website ook volledig
                responsive gemaakt dus
                de gebruiker kan de website ook op kleinere schermen bekijken<br><br>

                <strong>Leerdoelen</strong> <br><br>
                <i>Design:</i><br>
                Zoals je op de foto's hierboven kunt zien heb ik wireframes gemaakt. Ik moest wat keuzes maken tijdens
                het maken van de wireframes, aangezien je op de laatste 2 slides kunt zien dat ik screenshots heb van 2
                dezelfde leerresultaten, maar dat is niet het geval. Ik moest kiezen tussen een aparte pagina voor een
                project of een modal (pop-up). Ik heb uiteindelijk voor een aparte pagina gekozen omdat ik op een aparte
                pagina meer ruimte heb dan op een modal.
                <br><br>
                <i>Development:</i><br>
                Ook heb ik de website responsive gemaakt met behulp van Grid, Flexbox en Media queries. Normaal
                gebruikte ik altijd Bootstrap om de website responsive te maken, maar deze keer lieten de docenten me
                het niet gebruiken want ze wouden dat ik iets nieuws moet uitproberen. Het werken met mediaquery's was
                ook nieuw voor mij en daar heb ik veel van geleerd.
