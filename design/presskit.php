            <p class="col col-main">
                <strong>Project</strong> <br><br>
                Een van de wensen van Ruby Mus was een presskit.
                Ik wist niet wat een presskit precies inhield dus dit was het eerste wat ik moest
                achterhalen. Nadat ik erachter was wat een presskit precies inhield en waarom artiesten gebruik maken
                van een presskit begon ik aan het ontwerp. De presskit moest bij de rest van de website passen en
                dus moest er gelet worden op de huisstijl.

                <br><br>
                Nadat er besloten was dat we gingen voor een electronic presskit heb ik vooral
                gekeken naar ontwerpen en de dingen die er in moesten komen te staan. Hieruit bleek dat een presskit
                vaak de volgende lay-out kent; De pagina begint met een stukje over de artiest en soms een quote, Daarna
                staan er verschillende foto's die de pers en andere media kunnen gebruiken voor posters en artikelen.
                Ook staat er in een electronic presskit vaak een of meerdere sample tracks en (muziek) video’s. Het
                laatste dat in een presskit moet staan zijn contact gegevens zodat er contact opgenomen kan worden voor
                bijvoorbeeld interviews, vragen, fotoshoots en meer foto’s/video’s of audio fragmenten. Tijdens het
                ontwerpen heb ik gekeken naar de rest van de website en de stijl zodat ik deze kon gebruiken op het
                presskit ontwerp en alles mooi bij elkaar zou passen.
                <br><br>

                <strong>Leerdoelen</strong> <br><br>
                <i>Research:</i><br>
                Ik had eerst geen idee wat een presskit was, dus heb ik verkennend onderzoek gedaan naar wat een
                presskit was en wat voor informatie erin moet worden weergegeven om welke redenen.
                <br><br>
                <i>Design:</i><br>
                Voor de presskit van Ruby Mus heb ik verschillende design keuzes moeten maken vooral met betrekking tot
                de
                lay-out van alle weergegeven informatie, ik heb me voor het ontwerp van de webpagina gehouden aan de
                rest van de website en de presskit. Bij het maken van deze presskit heb ik gebruik gemaakt van Adobe
                PhotoShop.
