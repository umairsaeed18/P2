                <strong>Project</strong> <br><br>
                Voor sprint X was het de bedoeling om in 3 weken een project te maken. Het duurde niet lang voordat ik
                met een project op de proppen kwam. Ik wilde echt iets doen met de API's. Omdat ik zelf ook wat
                cryptomunten heb, kwam ik op het idee om een cryptografiek te maken. Een tabel met informatie over
                verschillende cryptocurrencies die afkomstig is van API<br><br>

                <strong>Leerdoelen</strong> <br><br>
                <i>Development:</i><br>
                Voordat ik aan dit project begon, had ik helemaal geen kennis van JSON-calls. Ik moest veel zoeken en
                proberen (trial & error).
                Toen ben ik op zoek gegaan naar een API en ik had veel keuze maar ik koos voor de coincap omdat je bij
                deze geen API key nodig hebt.
                Het gebruiken van een API met een sleutel had meer nadelen dan voordelen omdat ik moest wachten tot ik
                de sleutel opgestuurd kreeg en daar geen tijd voor had.
                <br><br>
                Connectie maken met de API was niet zo heel moeilijk. Na de connectie heb ik met JavaScript de data
                opgehaald die ik nodig had
                voor mijn cryptotabel. Ik wou het munteenheid als Euro hebben maar dat was helaas niet mogelijk met deze
                API dus daarom heb ik
                deze in Dollar laten zien. <br><br>
                Voor de extra informatie heb ik mustache.js gebruikt. Met mustache.js kunt u eenvoudig sjablonen maken
                om de
                gegevens rechtstreeks vanuit de API weer te geven op HTML. Ik heb ook Chart.js gebruikt om een grafiek
                van de
                prijsdaling of -stijging te tonen van een munt en vervolgens in een modal gezet.
                <br><br>

                <i>Design:</i><br>
                Tijdens het maken van dit project heb ik het programma Adobe XD gebruikt om ontwerpen te maken zodat ik
                een idee heb hoe de webpagina eruit komt te zien.
                Die ontwerpen heb ik bij de projecten van leeruitkomst "Design" togevoegd.
                <br><br>

                <i>Research:</i><br>
                Voor dit project moest ik eerst uitzoeken wat voor soort API ik ging gebruiken. Daarna deed ik nog 2
                onderzoeken naar het API-gebruik en de invloed van media op crypto. Ik wist bijna niets over API's,
                terwijl ik dit project maakte, heb ik er veel over geleerd.
                De onderzoeken heb ik bij de projecten van leeruitkomst "Research" togevoegd.

                <br><br>

