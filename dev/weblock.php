            <p class="col col-main">
                <strong>Project</strong> <br><br>
                In de "password-container" wordt het gegenereerde wachtwoord weergegeven samen met een knop om het
                wachtwoord naar
                het klembord te kopiëren. Het stelt gebruikers ook in staat om een wachtwoordlengte op te geven en of
                het wachtwoord cijfers of symbolen moet bevatten.
                <br><br>

                Het eerste wat ik deed was de karakters definiëren die het wachtwoord kan bevatten. Een sterk wachtwoord
                moet een combinatie van kleine letters en hoofdletters, cijfers en symbolen bevatten. Verder zijn er
                verschillende functies gebruikt die de tekstvelden (wachtwoordlengte) inlezen en het lezen vervolgens
                combineren met wat de gebruiker heeft aagevinkt.
                <br><br>

                <strong>Leerdoelen</strong> <br><br>
                <i>Development:</i><br>
                Ik heb veel geleerd tijdens het maken van dit project. Voordat ik aan dit project begon, wist ik niet
                dat het zo makkelijk was om letters en cijfers op een webpagina te controleren. Ik kwam er ook achter
                dat het ook heel makkelijk is om de letters om te zetten in hoofdletters of kleine letters. Ik heb ook
                voor het eerst het functie "kopiëren naar klembord" gemaakt.
                <br><br>

                <i>Interactive Media:</i><br>
                Eindgebruiker kan op vele manieren interactie hebben op de website, hij kan bijvoorbeeld de
                selectievakjes aanvinken, hij kan de lengte van het wachtwoord invullen en hij kan het wachtwoord ook
                kopiëren.
                <br><br>
                <i>Design:</i><br>
                Op de foto's kunt u zien dat ik verschillende ontwerpen had voor mijn webpagina. Uiteindelijk heb ik
                voor de gele gekozen omdat ik verschillende websites heb bekeken die ongeveer hetzelfde werken als mijn
                website en ik ontdekte dat de meeste websites felle kleuren gebruiken.
