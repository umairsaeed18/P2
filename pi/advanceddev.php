                <strong>Project</strong> <br><br>
                Het project voor advanced development bestaat uit drie weken, waarbij je je elke week iets verder
                verdiept in JavaScript Libraries.<br><br>
                Week 1 <br>
                Voor de eerste week moesten we gaan zoeken naar 10 JavaScript libraries en daarvan uitzoeken en
                beschrijven wat ze deden en dit allemaal op een pagina zetten zodat de docenten dit konden zien.

                <br><br>
                Week 2 <br>
                Voor week twee moesten we ons verdiepen in drie van de libraries die we in week een hadden uitgekozen.
                Ik besloot te gaan voor: Typewriter.js, een library voor een cool typewriter-effect; Aos.js, een library
                voor het
                animeren van een elementen met de scroll functie; Anime.js, een library om makkelijk en snel elementen
                te laten animeren.

                <br><br>
                Week 3 <br>
                Voor week 3 was het de bedoeling dat je liet zien dat je instaat bent tot advenced JavaScript, door
                gebruik te maken van libraries en eigen code.
                <br><br>

                <strong>Leerdoelen</strong> <br><br>
                <i>Development:</i><br>
                In dit project heb ik gebruik gemaakt van de kennis die ik heb opgedaan tijdens het verloop van dit
                project en de eerder gemaakte POC's. Ook heb ik gebruik gemaakt van een versie beheer tool om de
                voortgang bij te houden. Ook heb ik gebruik leren maken van libraries en hoe ik deze kan combineren.
                <br><br>

                <i>Professional Identity:</i><br>
