                <strong>Project</strong> <br><br>
                De opdracht was om een merkonderzoek te doen over bepaalde merken. Ik koos voor Scotch & Soda. Het
                verslag moest ook netjes worden opgemaakt, dus ik gebruikte Adobe XD om het document goed te
                stylen met ongeveer dezelfde huisstijl van het merk zelf.<br><br>
                <strong>Leerdoelen</strong> <br><br>
                <i>Design:</i><br>
                Zoals je op de foto's hierboven kunt zien, heb ik het verslag gemaakt in Adobe XD. Ik heb voor Adobe XD
                gekozen omdat je je eigen fonts kunt uploaden en je werk ook op verschillende schermen kunt bekijken. Ik
                heb bijna alle documenten opgemaakt en gemaakt
                met AdobeXD. Voor dit project heb ik geprobeerd vergelijkbare kleuren en lettertypen te gebruiken als
                Scotch & Soda.
                <br><br>
                <i>Research en Professional Identity:</i><br>
                Branding was de specialisatie die ik deed naast geavanceerde ontwikkeling. Voor dit project moesten we
                een merk onderzoeken. We moesten kiezen uit:<br>
                <i>Goose Island Beer Co. (Bierbrouwerij)<br>
                    Call of Duty: Modern Warfare (spel)<br>
                    Scotch & Soda (kledingmerk)<br>
                    Coffeelab (horecaorganisatie)<br>
                    Netflix (streamingdienst)<br></i>
                <br>
                Ik had voor Scotch & Soda gekozen omdat dit een wereldberoemd Nederlands merk is. Het rapport mocht
                maximaal 4 pagina's/dia's lang zijn en het citaat moest voldoen aan de <a
                    href="https://www.studiemeesters.nl/studietips/wat-apa-een-korte-uitleg-voor-beginners/">APA-richtlijnen.</a>
                In het begin had ik geen idee wat APA was, dus ging ik het opzoeken. Na mijn onderzoek kwam ik erachter
                dat APA niets meer was dan "speciale citatieregels".
                <br><br>Naast APA moesten we ook een brand research tool
                gebruiken zoals een Brand Canvas en/of een <a
                    href="https://gedragvandeconsument.nl/brand-identity-prism/">Brand Identity Prism.</a> Ik had
                gekozen voor Brand Identity
                Prism en dat betekent dat je je merk moet omschrijven op de volgende onderwerpen:
                Fysiek
                Relatie
                Reflectie
                Zelfbeeld
                Cultuur
                Persoonlijkheid.
